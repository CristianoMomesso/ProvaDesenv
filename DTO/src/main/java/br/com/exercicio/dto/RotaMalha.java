public class RotaMalha {
	/**
	 * Atributo p1 do tipo String
	 */
	private String p1;
	/**
	 * Atributo p2 do tipo String
	 */
	private String p2;
    /**
     * Atributo distancia do tipo double
     */
    private double distancia;
    
	/**
	 * @return p1
	 */
	public String getP1() {
		return p1;
	}
	/**
	 * @param p1
	 */
	public void setP1(String p1) {
		this.p1 = p1;
	}
	/**
	 * @return p2
	 */
	public String getP2() {
		return p2;
	}
	/**
	 * @param p2
	 */
	public void setP2(String p2) {
		this.p2 = p2;
	}
	/**
	 * @return distancia
	 */
	public double getDistancia() {
		return distancia;
	}
	/**
	 * @param distancia
	 */
	public void setDistancia(double distancia) {
		this.distancia = distancia;
	}	
}
